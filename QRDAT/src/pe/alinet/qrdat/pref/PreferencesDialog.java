    private void btnSetRecordsDirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSetRecordsDirActionPerformed
        JFileChooser fc = new JFileChooser();
        fc.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int returnVal = fc.showOpenDialog(this);
        
        if (returnVal == JFileChooser.APPROVE_OPTION){
            File file = fc.getSelectedFile();
            txtRecordsDir.setText(file.getAbsolutePath());
            
        }
    }//GEN-LAST:event_btnSetRecordsDirActionPerformed

